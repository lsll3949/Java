package step07;

public class SamsungTv {
	public void turnOn() {
		System.out.println("Samsung TV - 전원을 켠다");
	}
	public void turnOff() {
		System.out.println("Samsung TV - 전원을 끈다");
	}
	public void soundUP() {
		System.out.println("Samsung TV - 소리를 높인다");
	}	
	public void soundDown() {
		System.out.println("Samsung TV - 소리를 낮춘다");
	}	
}
